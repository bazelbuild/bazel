package com.google.devtools.build.lib.skyframe.packages;

import static com.google.common.base.Preconditions.checkState;

import com.google.common.collect.ImmutableList;
import com.google.common.collect.ImmutableMap;
import com.google.devtools.build.lib.cmdline.Label;
import com.google.devtools.build.lib.cmdline.PackageIdentifier;
import com.google.devtools.build.lib.cmdline.RepositoryMapping;
import com.google.devtools.build.lib.events.Event;
import com.google.devtools.build.lib.packages.NoSuchPackageException;
import com.google.devtools.build.lib.packages.Package;
import com.google.devtools.build.lib.server.FailureDetails.FailureDetail;
import com.google.devtools.build.lib.skyframe.BzlLoadFailedException;
import com.google.devtools.build.lib.skyframe.DetailedException;
import com.google.devtools.build.lib.util.ValueOrException;
import java.util.Optional;
import javax.annotation.Nullable;
import javax.annotation.concurrent.NotThreadSafe;
import net.starlark.java.eval.Module;

/** A standalone library for performing Bazel package loading. */
public interface PackageLoader extends AutoCloseable {
  /**
   * Loads and returns a single package. This method is a simplified shorthand for {@link
   * #loadPackages} when just a single {@link Package} and nothing else is desired.
   */
  Package loadPackage(PackageIdentifier pkgId) throws NoSuchPackageException, InterruptedException;

  /**
   * Returns the result of loading a collection of packages. Note that the returned {@link Package}s
   * may contain errors - see {@link Package#containsErrors()} for details.
   */
  Result loadPackages(Iterable<PackageIdentifier> pkgIds) throws InterruptedException;

  /**
   * A reusable context for loading Starlark modules (i.e. .bzl or .scl files), intended for use by
   * standalone implementations of the starlark_doc_extract rule.
   *
   * <p>Instances of this class are not thread-safe; but the values loaded and returned by its
   * methods are thread-safe. The intention is for StarlarkModuleLoadingContext to be used e.g. as a
   * local variable in a single-threaded wrapper method of a higher-level abstraction.
   */
  // TODO(arostovtsev): move loadPackages into StarlarkModuleLoadingContext - and rename it to
  // LoadingContext.
  @NotThreadSafe
  interface StarlarkModuleLoadingContext {
    /**
     * Returns the result of loading a collection of Starlark modules (i.e. .bzl or .scl files).
     *
     * <p>A call to this method clears the list of events accumulated by previous {@link
     * #loadModules} calls.
     */
    ImmutableMap<Label, ValueOrException<Module, StarlarkModuleLoadingException>> loadModules(
        Iterable<Label> labels) throws InterruptedException;

    /**
     * Returns the main repository's repository mapping.
     *
     * <p>Intended to be used for stringifying labels in loaded modules.
     */
    RepositoryMapping getRepositoryMapping() throws InterruptedException;

    /** Returns all events generated by the last {@link #loadModules} call. */
    ImmutableList<Event> getEvents();
  }

  /** An exception thrown when we fail to load a Starlark module. */
  class StarlarkModuleLoadingException extends Exception {
    StarlarkModuleLoadingException(String message) {
      super(message);
    }

    StarlarkModuleLoadingException(BzlLoadFailedException cause) {
      super(cause);
    }

    /** The {@link FailureDetail} of the underlying exception, if one is available. */
    Optional<FailureDetail> getFailureDetail() {
      if (getCause() instanceof DetailedException) {
        return Optional.of(
            ((DetailedException) getCause()).getDetailedExitCode().getFailureDetail());
      } else {
        return Optional.empty();
      }
    }
  }

  /** Returns a new {@link StarlarkModuleLoadingContext}. */
  StarlarkModuleLoadingContext makeStarlarkModuleLoadingContext() throws InterruptedException;

  /**
   * Shut down the internal threadpools used by the {@link PackageLoader}.
   *
   * <p>Call this method when you are completely done with the {@link PackageLoader} instance,
   * otherwise there may be resource leaks.
   */
  @Override
  void close();

  /** Contains the result of package loading. */
  // TODO(arostovtsev): replace with ImmutableMap<PackageIdentifier, ValueOrException<Package,
  // NoSuchPackageException>>
  class Result {
    private final ImmutableMap<PackageIdentifier, PackageOrException> loadedPackages;
    private final ImmutableList<Event> events;

    Result(
        ImmutableMap<PackageIdentifier, PackageOrException> loadedPackages,
        ImmutableList<Event> events) {
      this.loadedPackages = loadedPackages;
      this.events = events;
    }

    public ImmutableMap<PackageIdentifier, PackageOrException> getLoadedPackages() {
      return loadedPackages;
    }

    /** Returns all events generated while loading the requested packages. */
    public ImmutableList<Event> getEvents() {
      return events;
    }
  }

  /** Contains a {@link Package} or the exception produced while loading it. */
  // TODO(arostovtsev): replace with ValueOrException<Package, NoSuchPackageException>
  class PackageOrException {
    private final Package pkg;
    private final NoSuchPackageException exception;

    PackageOrException(@Nullable Package pkg, @Nullable NoSuchPackageException exception) {
      checkState((pkg == null) != (exception == null));
      this.pkg = pkg;
      this.exception = exception;
    }

    /**
     * @throws NoSuchPackageException if there was a non-recoverable error loading the package, e.g.
     *     an io error reading the BUILD file.
     */
    public Package get() throws NoSuchPackageException {
      if (pkg != null) {
        return pkg;
      }
      throw exception;
    }
  }
}
