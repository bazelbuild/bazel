  /** Tests that the error message is produced if the mercurial repository does not exist. */
  @Test
  public void testhgRepositoryErrorMessage() throws Exception {
    context()
        .write(
            "MODULE.bazel",
            "mercurial_repository = use_repo_rule(\"@bazel_tools//tools/build_defs/repo:hg.bzl\","
                + " \"mercurial_repository\")",
            "mercurial_repository(",
            "  name='ext',",
            "  remote='file:///some_path',",
            "  revision='some_hash',",
            ")");

    // This creates Bazel without MSYS, see implementation for details.
    BuilderRunner bazel = WorkspaceTestUtils.bazel(context());
    ProcessResult result = bazel.shouldFail().build("@ext//:write_text");
    assertThat(result.errString()).contains("error running 'hg clone");
    assertThat(result.errString()).contains("abort: repository /some_path not found");
  }

  private static String setuphgRepository(BlackBoxTestContext context, Path repo)
      throws Exception {
    HgRepositoryHelper hgRepository = initHgRepository(context, repo);

    RepoWithRuleWritingTextGenerator generator = new RepoWithRuleWritingTextGenerator(repo);
    generator.skipBuildFile().setupRepository();

    hgRepository.addAll();
    hgRepository.commit("Initial commit");
    hgRepository.tag("first");
    return hgRepository.getHead();
  }

  private static HgRepositoryHelper initHgRepository(BlackBoxTestContext context, Path repo)
      throws Exception {
    PathUtils.deleteTree(repo);
    Files.createDirectories(repo);
    HgRepositoryHelper hgRepository = new HgRepositoryHelper(context, repo);
    hgRepository.init();
    return hgRepository;
  }
