package com.google.devtools.build.lib.query2;

import static com.google.common.collect.ImmutableList.toImmutableList;
import static com.google.common.truth.Truth.assertThat;

import com.google.common.collect.ImmutableList;
import com.google.devtools.build.lib.packages.Target;
import com.google.devtools.build.lib.packages.util.MockToolsConfig;
import com.google.devtools.build.lib.query2.engine.QueryEnvironment.QueryFunction;
import com.google.devtools.build.lib.query2.testutil.AbstractQueryTest.QueryHelper.ResultAndTargets;
import com.google.devtools.build.lib.query2.testutil.SkyframeQueryHelper;
import java.io.IOException;
import java.util.Set;
import org.junit.After;
import org.junit.Before;
import org.junit.Test;
import org.junit.runner.RunWith;
import org.junit.runners.JUnit4;

/** Integration tests for {@link ExtensionInputsFunction}. */
@RunWith(JUnit4.class)
public final class ExtensionInputsFunctionTest {

  // The canonical name assigned by Bazel to "com_github_foo" generated by "go_deps"
  // extension defined in the root module's //:extensions.bzl.
  // Format: {rootModuleName}+{extensionName}+{internalRepoName}
  // Root module has empty string name in canonical form, so prefix is "+go_deps".
  private static final String COM_GITHUB_FOO_CANONICAL = "+go_deps+com_github_foo";

  private SkyframeQueryHelper helper;

  @Before
  public void setUp() throws Exception {
    helper =
        new SkyframeQueryHelper() {
          @Override
          protected String getRootDirectoryNameForSetup() {
            return "/workspace";
          }

          @Override
          protected void performAdditionalClientSetup(MockToolsConfig mockToolsConfig)
              throws IOException {
            // Overwrite MODULE.bazel so the "go_deps" extension is active from the start.
            overwriteFile(
                "MODULE.bazel",
                "module(name='root', version='1.0')",
                "go_deps = use_extension('//:extensions.bzl', 'go_deps')",
                "use_repo(go_deps, 'com_github_foo')");

            // Pre-write the lockfile so BazelLockFileValue.KEY is populated with extension data
            // from the first Skyframe evaluation. extension_inputs() reads BazelLockFileValue,
            // which is computed from the lockfile on disk. Without this, the lockfile is empty
            // when BazelLockFileValue.KEY is first evaluated (before the extension runs), because
            // the lockfile is only updated by BazelLockFileModule.afterCommand() which runs after
            // the query completes.
            //
            // Key format: "@@<bzlLabel>%<extensionName>" where @@ is the root module's canonical
            // repo prefix and the inner key "general" = platform-independent eval factors.
            // "FILE://go.mod abc123" = a recorded FILE input; the hash is ignored by
            // extension_inputs() which only uses the path component.
            overwriteFile(
                "MODULE.bazel.lock",
                "{",
                "  \"lockFileVersion\": 26,",
                "  \"registryFileHashes\": {},",
                "  \"selectedYankedVersions\": {},",
                "  \"moduleExtensions\": {",
                "    \"@@//:extensions.bzl%go_deps\": {",
                "      \"general\": {",
                "        \"bzlTransitiveDigest\": \"\",",
                "        \"usagesDigest\": \"\",",
                "        \"recordedInputs\": [",
                "          \"FILE://go.mod abc123\"",
                "        ],",
                "        \"generatedRepoSpecs\": {",
                "          \"com_github_foo\": {",
                "            \"repoRuleId\": \"@@//:repo_rule.bzl%simple_repo\",",
                "            \"attributes\": {}",
                "          }",
                "        }",
                "      }",
                "    }",
                "  },",
                "  \"facts\": {}",
                "}");

            // A minimal repository rule that creates a BUILD file.
            writeFile(
                "repo_rule.bzl",
                "def _simple_repo_impl(ctx):",
                "  ctx.file('BUILD', 'exports_files([\"lib\"])')",
                "simple_repo = repository_rule(implementation=_simple_repo_impl)");

            // The extension reads go.mod (recording it as a file input) and creates com_github_foo.
            writeFile(
                "extensions.bzl",
                "load('//:repo_rule.bzl', 'simple_repo')",
                "def _go_deps_impl(module_ctx):",
                "  module_ctx.read(Label('//:go.mod'))",
                "  simple_repo(name='com_github_foo')",
                "go_deps = module_extension(implementation=_go_deps_impl)");

            // The workspace file whose path we expect extension_inputs() to return.
            writeFile("go.mod", "module example.com/root");

            // Root BUILD: export go.mod so it can appear as a query result.
            overwriteFile("BUILD", "exports_files(['go.mod'])");

            // A main-repo package that depends on the extension-generated repo.
            writeFile(
                "go/logger/BUILD",
                "filegroup(name='lib', srcs=['@com_github_foo//:lib'])");
          }

          @Override
          protected Iterable<QueryFunction> getExtraQueryFunctions() {
            return ImmutableList.of();
          }
        };

    helper.setUp();

    // SkyQueryEnvironment requires non-ordered results and a non-empty universe scope.
    helper.setOrderedResults(false);
    // Include both the main workspace AND the extension-generated repo in the universe so that
    // deps() traversal can find targets in @@+go_deps+com_github_foo.
    // The apparent name @com_github_foo is resolvable via the root module's repo mapping.
    helper.setUniverseScope("//...:*,@com_github_foo//...:*");
  }

  @After
  public void tearDown() {
    helper.cleanUp();
  }

  private Set<Target> eval(String query) throws Exception {
    ResultAndTargets<Target> result = helper.evaluateQuery(query);
    assertThat(result.getQueryEvalResult().getSuccess()).isTrue();
    return result.getResultSet();
  }

  // ── Test 1: Golden path ───────────────────────────────────────────────────

  @Test
  public void extensionInputs_returnsFilesForExternalRepoDeps() throws Exception {
    // //go/logger depends on @@+go_deps+com_github_foo//:lib.
    // The go_deps extension read go.mod to generate com_github_foo.
    // So extension_inputs(deps(//go/logger)) should surface //:go.mod.
    Set<Target> result = eval("extension_inputs(deps(//go/logger:lib))");

    assertThat(
            result.stream()
                .map(t -> t.getLabel().toString())
                .collect(toImmutableList()))
        .containsExactly("//:go.mod");
  }

  // ── Test 2: Main-repo targets return nothing ──────────────────────────────

  @Test
  public void extensionInputs_returnsEmpty_forMainRepoTarget() throws Exception {
    // //go/logger:lib is in the main repo; the function skips it.
    assertThat(eval("extension_inputs(//go/logger:lib)")).isEmpty();
  }

  // ── Test 3: No lockfile / empty lockfile returns nothing ──────────────────

  @Test
  public void extensionInputs_returnsEmpty_whenNoLockfile() throws Exception {
    // An empty set() produces no targets to inspect.
    assertThat(eval("extension_inputs(set())")).isEmpty();
  }

  // ── Test 4: Composability with deps() ────────────────────────────────────

  @Test
  public void extensionInputs_worksWithDepsFunction() throws Exception {
    // deps() returns the transitive closure; extension_inputs wraps the result.
    // When the only external dep is @@+go_deps+com_github_foo, we get go.mod back.
    Set<Target> result = eval("extension_inputs(deps(//go/logger:lib))");

    assertThat(
            result.stream()
                .map(t -> t.getLabel().toString())
                .collect(toImmutableList()))
        .containsExactly("//:go.mod");
  }
}
